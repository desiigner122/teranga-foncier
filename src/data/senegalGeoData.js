// Fonctions utilitaires pour la gestion géographique
export const geoUtils = {
  // Récupérer toutes les régions
  getAllRegions: () => {
    return senegalGeoData.regions.map(region => ({
      code: region.code,
      name: region.name
    }));
  },

  // Récupérer les départements d'une région
  getDepartmentsByRegion: (regionCode) => {
    const region = senegalGeoData.regions.find(r => r.code === regionCode);
    return region ? region.departments.map(dept => ({
      code: dept.code,
      name: dept.name
    })) : [];
  },

  // Récupérer les communes d'un département
  getCommunesByDepartment: (regionCode, departmentCode) => {
    const region = senegalGeoData.regions.find(r => r.code === regionCode);
    if (!region) return [];
    
    const department = region.departments.find(d => d.code === departmentCode);
    return department ? department.communes.map(commune => ({
      name: commune,
      fullPath: `${region.name} > ${department.name} > ${commune}`
    })) : [];
  },

  // Recherche de commune par nom
  searchCommune: (query) => {
    const results = [];
    senegalGeoData.regions.forEach(region => {
      region.departments.forEach(dept => {
        dept.communes.forEach(commune => {
          if (commune.toLowerCase().includes(query.toLowerCase())) {
            results.push({
              commune,
              department: dept.name,
              region: region.name,
              fullPath: `${region.name} > ${dept.name} > ${commune}`
            });
          }
        });
      });
    });
    return results;
  },

  // Valider une localisation complète
  validateLocation: (regionCode, departmentCode, communeName) => {
    const region = senegalGeoData.regions.find(r => r.code === regionCode);
    if (!region) return false;
    
    const department = region.departments.find(d => d.code === departmentCode);
    if (!department) return false;
    
    return department.communes.includes(communeName);
  }
};
